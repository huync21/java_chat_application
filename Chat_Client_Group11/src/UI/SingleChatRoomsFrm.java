    public void receiveRoomsAndDisplayData(ArrayList<Room> listRoom){
        listSingleChatRooms = listRoom;
        // hien thi
        String[][] data = new String[listSingleChatRooms.size()][4];
        String[] columnNames = {"id room","user name","họ tên","online status"};
        for(int i=0;i<listSingleChatRooms.size();i++){
            Room room = listSingleChatRooms.get(i);
            data[i][0] = room.getId()+"";
            
            // lay ra ten cua user con lai trong phong
            User currenUser = clientProcess.getUser();
            User theRestUserInTheRoom = new User();
            ArrayList<UserInARoom> listUsersInSingleChatRoom = room.getListUserInARoom();
            for(UserInARoom u : listUsersInSingleChatRoom){
                if(u.getUser().getId() != currenUser.getId()) theRestUserInTheRoom = u.getUser();
            }
            data[i][1] = theRestUserInTheRoom.getUserName();
            data[i][2] = theRestUserInTheRoom.getFullName();
            data[i][3] = theRestUserInTheRoom.getOnlineStatus()+"";
        }
        DefaultTableModel dtm = new DefaultTableModel(data, columnNames);
        tblRooms.setModel(dtm);
        
        btnSearchForUser.addActionListener((e) -> {
            new SearchUserForSingleChatFrm(clientProcess).setVisible(true);
            this.dispose();
        });
        
        tblRooms.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int column = tblRooms.getColumnModel().
                        getColumnIndexAtX(e.getX()); // 
                int row = e.getY()/tblRooms.getRowHeight(); 
 
                if (row < tblRooms.getRowCount() && row >= 0 && 
                            column < tblRooms.getColumnCount() && column >= 0) {
                   Room room = listSingleChatRooms.get(row);
                   // set room cho tien trinh clientHandler
                   clientProcess.setRoom(room);
                   new SingleChatFrm(clientProcess).setVisible(true);
                   mainFrame.dispose();
                }
            }
        });
    }

